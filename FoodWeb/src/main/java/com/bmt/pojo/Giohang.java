public class Giohang {
    private Integer idmonan;
    private String tenmonan;
    private Double gia;
    private Double soluong;
    private String anhmonan;
    private Double tongtien;
    /**
     * @return the idmonan
     */
    public Integer getIdmonan() {
        return idmonan;
    }

    /**
     * @param idmonan the idmonan to set
     */
    public void setIdmonan(Integer idmonan) {
        this.idmonan = idmonan;
    }

    /**
     * @return the tenmonan
     */
    public String getTenmonan() {
        return tenmonan;
    }

    /**
     * @param tenmonan the tenmonan to set
     */
    public void setTenmonan(String tenmonan) {
        this.tenmonan = tenmonan;
    }

    /**
     * @return the gia
     */
    public double getGia() {
        return gia;
    }

    /**
     * @param gia the gia to set
     */
    public void setGia(double gia) {
        this.gia = gia;
    }

    /**
     * @return the soluong
     */
    public Double getSoluong() {
        return soluong;
    }

    /**
     * @param soluong the soluong to set
     */
    public void setSoluong(Double soluong) {
        this.soluong = soluong;
    }

    /**
     * @return the anhmonan
     */
    public String getAnhmonan() {
        return anhmonan;
    }

    /**
     * @param anhmonan the anhmonan to set
     */
    public void setAnhmonan(String anhmonan) {
        this.anhmonan = anhmonan;
    }

    /**
     * @return the tongtien
     */
    public Double getTongtien() {
        return tongtien;
    }

    /**
     * @param tongtien the tongtien to set
     */
    public void setTongtien(Double tongtien) {
        this.tongtien = tongtien;
    }
}
